package data;

public class Fvrt_Feed {
    private String id;
    private String title;

    public Fvrt_Feed() {
    }

    public Fvrt_Feed(String id2, String title2) {
        id = id2;
        title = title2;
    }

    public String getid() {
        return id;
    }

    public void setid(String id2) {
        id = id2;
    }

    public String gettitle() {
        return title;
    }

    public void settitle(String title2) {
        title = title2;
    }
}
